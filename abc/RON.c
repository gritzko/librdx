ok64 RONutf8sFeed(u8** into, ok64 val) {
    u8 tmp[11];
    u8* e = tmp + 11;
    u8* t = e;
    do {
        *--t = RON64_CHARS[val & 63];
        val >>= 6;
    } while (val);
    size_t l = e - t;
    if ($len(into) < l) return SNOROOM;
    memcpy(*into, t, l);
    *into += l;
    return OK;
}

ok64 RONutf8sDrain(ok64* o, u8c* const* from) {
    ok64 res = 0;
    for (u8c* p = from[0]; p < from[1]; ++p) {
        u64 v = RON64_REV[*p];
        if (v == 0xff) return OKbadtext;
        res = (res << 6) | v;
    }
    *o = res;
    return OK;
}

ok64 RONOfTime(ron60* r, struct tm* t) {
    if (!r || !t) return BADARG;
    u64 y = t->tm_year - 100;
    if (y >= 100) return BADARG;
    if (t->tm_mon < 0 || t->tm_mon >= 12) return BADARG;
    if (t->tm_mday < 1 || t->tm_mday > 31) return BADARG;
    if (t->tm_hour < 0 || t->tm_hour >= 24) return BADARG;
    if (t->tm_min < 0 || t->tm_min >= 60) return BADARG;
    if (t->tm_sec < 0 || t->tm_sec >= 60) return BADARG;
    *r = 0;
    *r |= ((y / 10) << (9 * 6));
    *r |= ((y % 10) << (8 * 6));
    *r |= ((u64)(t->tm_mon + 1) << (7 * 6));
    *r |= ((u64)(t->tm_mday) << (6 * 6));
    *r |= ((u64)(t->tm_hour) << (5 * 6));
    *r |= ((u64)(t->tm_min) << (4 * 6));
    *r |= ((u64)(t->tm_sec) << (3 * 6));
    return OK;
}

ok64 RONToTime(ron60 r, struct tm* t) {
    if (!t) return BADARG;
    u64 y1 = (r >> (9 * 6)) & 63;
    u64 y0 = (r >> (8 * 6)) & 63;
    u64 mon = (r >> (7 * 6)) & 63;
    u64 mday = (r >> (6 * 6)) & 63;
    u64 hour = (r >> (5 * 6)) & 63;
    u64 min = (r >> (4 * 6)) & 63;
    u64 sec = (r >> (3 * 6)) & 63;
    if (y1 >= 10 || y0 >= 10) return BADARG;
    if (mon < 1 || mon > 12) return BADARG;
    if (mday < 1 || mday > 31) return BADARG;
    if (hour >= 24) return BADARG;
    if (min >= 60) return BADARG;
    if (sec >= 60) return BADARG;
    t->tm_year = 100 + y1 * 10 + y0;
    t->tm_mon = mon - 1;
    t->tm_mday = mday;
    t->tm_hour = hour;
    t->tm_min = min;
    t->tm_sec = sec;
    return OK;
}

ok64 RONVerify(u8c** txt) {
    if (txt[0] >= txt[1]) return RONBAD;
    for (u8cp c = txt[0]; c < txt[1]; ++c) {
        if (RON64_REV[*c] == 0xff) return RONBAD;
    }
    return OK;
}

ok64 RONu8sFeedPad(u8** into, ok64 val, u8 width) {
    if ($len(into) < width) return SNOROOM;
    u8p p = into[0] + width;
    for (u8 i = 0; i < width; i++) {
        *--p = RON64_CHARS[val & 63];
        val >>= 6;
    }
    if (val != 0) return SBADARG;
    return OK;
}

ok64 RONSpliceBase(ok64 *base, u8 *width, u64 rand, u64 prob, ok64 n) {
    if (n == 0 || prob == 0) return SBADARG;
    u64 need = 2 * prob * n;
    u8 w = 1;
    u64 space = 64;
    while (space < need && w < 10) {
        space *= 64;
        w++;
    }
    *width = w;
    u64 avail = space - n;
    *base = rand % avail;
    return OK;
}
